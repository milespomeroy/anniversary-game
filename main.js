function getRandomInt (min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function getRandomImage() {
  var rand = getRandomInt(0, data.length - 1);
  return data.splice(rand, 1)[0];
}

var currentImage = getRandomImage();
var score = 0;
var total = 1;

$(function () {
  var image = $('img', '#current-image').attr('src', currentImage.url);

  $('button', '#years').click(function () {
    var guess = $(this).text();
    if(guess == currentImage.year) {
      score++;
      $('#grade').text('Correct').addClass('correct').removeClass('wrong');
    } else {
      $('#grade').text('Wrong').removeClass('correct').addClass('wrong');
    }

    $('#correct-answer').text(currentImage.year);
    $('#current-score').text(score + '/' + total);

    $('#years').hide();
    $('#reveal').show();

    if(data.length === 0) {
      $('.pager').hide();
      $('#game-over').show();
    }
  });

  $('#next').click(function() {
    currentImage = getRandomImage();
    image
      .attr('src', '')
      .attr('src', currentImage.url);

    total++;

    $('#years').show();
    $('#reveal').hide();
  });
});




